/** 分辨率 → 算力倍率；costPerGeneration 为 720p 基准 */
export const RESOLUTION_COST_MULTIPLIER: Record<string, number> = {
  '720p': 1,
  '1080p': 2,
  '2k': 3,
  '4k': 4,
};

export function getResolutionCost(baseCost: number, resolution: string): number {
  return baseCost * (RESOLUTION_COST_MULTIPLIER[resolution] ?? 2);
}

