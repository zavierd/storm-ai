export type ToolCategory = 'interior_ai' | 'super_ai' | 'toolbox' | 'human';

export interface ParamOption {
  label: string;
  value: string;
}

export interface ToolParam {
  key: string;
  label: string;
  type: 'select' | 'slider' | 'text' | 'toggle';
  options?: ParamOption[];
  min?: number;
  max?: number;
  step?: number;
  defaultValue: string | number | boolean;
}

export interface ToolConfig {
  id: number;
  slug: string;
  title: string;
  featureKey: string;
  category: ToolCategory;
  image: string;
  /** 是否需要上传图片，默认 true；文生图工具为 false */
  requiresImage?: boolean;
  uploadLabel: string;
  uploadHint: string;
  hasPrompt: boolean;
  promptPlaceholder?: string;
  hasResolution: boolean;
  hasAspectRatio: boolean;
  hasDualImage: boolean;
  dualImageLabel?: string;
  /** 双图工具是否强制要求第二张图，默认 true；collage-render 为 false（第二张可选） */
  requiresSecondImage?: boolean;
  hasRoomSelect: boolean;
  hasRegion: boolean;
  regionHint?: string;
  customParams: ToolParam[];
  costPerGeneration: number;
  outputType: 'image' | 'text' | 'mixed';
}

export type EngineTypeKey =
  | 'gemini_direct'
  | 'vertex'
  | 'venice'
  | 'newapi'
  | 'swiftask'
  | string;

